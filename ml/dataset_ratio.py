import pandas as pd

# ==============================================================================
# СКРИПТ ЗА АНАЛИЗ НА БАЛАНСА В ДАННИТЕ (DATASET RATIO)
# Този скрипт зарежда данните за обучение от CSV файл и изчислява 
# разпределението (брой и процент) на отделните класове.
# Проверява дали класовете са балансирани и дава препоръки при нужда.
# ==============================================================================

# 1. Зареждане на данните
# Дефиниране на пътя до CSV файла с данните за обучение.
csv_path = "dataset.csv"  # Увери се, че пътят е верен
# Прочитане на CSV файла в pandas DataFrame структура.
df = pd.read_csv(csv_path)

# 2. Преброяване на класовете
# Използваме функцията value_counts(), която автоматично групира по уникална стойност 
# в колоната 'class_id' и връща броя на срещанията.
# sort_index() сортира резултатите по номера на класа (възходящо).
counts = df['class_id'].value_counts().sort_index()
# Изчисляваме общия брой на записите (редовете) в DataFrame.
total = len(df)

# Извеждане на заглавна част и заглавия на колоните в конзолата
print(f"📊 Общ брой точки в dataset-a: {total}\n")
print(f"{'ID на Клас':<15} | {'Брой точки':<15} | {'Процент':<10}")
print("-" * 45)

# 3. Изчисляване и принтиране на проценти
# Обхождаме всяка двойка (идентификатор на клас, брой точки)
for class_id, count in counts.items():
    # Пресмятаме процентното съотношение на класа спрямо общия брой точки
    percentage = (count / total) * 100
    # Отпечатваме форматиран ред с информацията
    print(f"{class_id:<15} | {count:<15} | {percentage:.2f}%")

print("-" * 45)

# 4. Бърза проверка за баланс
# Взимаме минималния и максималния брой точки сред всички класове.
min_class = counts.min()
max_class = counts.max()
# Изчисляваме съотношението между най-представения и най-слабо представения клас.
ratio = max_class / min_class

# Ако съотношението е по-голямо от 3, считаме че данните са силно небалансирани.
if ratio > 3:
    print(f"\n⚠️ ВНИМАНИЕ: Имаш сериозен дисбаланс!")
    print(f"Най-големият клас е {ratio:.1f} пъти по-голям от най-малкия.")
    print("Съвет: Добави още точки за най-малкия клас или използвай class_weight='balanced'.")
else:
    print("\n✅ Данните изглеждат сравнително балансирани.")