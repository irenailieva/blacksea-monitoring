import rasterio
import numpy as np
import sys

# ==============================================================================
# СКРИПТ ЗА ПРОВЕРКА НА СТОЙНОСТИ В TIF ФАЙЛ
# Този скрипт прочита географски TIF файл (подаден като аргумент от командния ред)
# и извежда списък с всички уникални стойности на пикселите, заедно с техния брой.
# ==============================================================================

# Взимане на пътя до файла от аргументите при стартиране на скрипта
path = sys.argv[1]

# Отваряне на TIF файла с библиотеката rasterio
with rasterio.open(path) as src:
    # Четене на първия канал (банд) от изображението
    data = src.read(1)
    
    # Намиране на уникалните стойности в масива и броя на техните срещания
    # np.unique връща два масива: един с уникалните стойности и един с честотата им
    unique, counts = np.unique(data, return_counts=True)
    
    # Отпечатване на резултата
    print(f"Уникални стойности във файла {path}:")
    # Обхождане на двойките (стойност, брой) едновременно чрез zip
    for v, c in zip(unique, counts):
        print(f"  Стойност {v}: среща се в {c} пиксела")
