import os
import glob
from image_processor import bake_png

# ==============================================================================
# СКРИПТ ЗА МИГРАЦИЯ НА TIF КЪМ PNG (MIGRATE PNGs)
# Този малък скрипт сканира директорията с резултати (TIF файлове)
# и за всеки класификационен TIF създава съответен PNG файл за бърза уеб визуализация,
# ако такъв все още не съществува.
# ==============================================================================

# Директорията, в която се намират генерираните TIF карти
inference_dir = "data/inference"
# Намиране на всички файлове завършващи на '_classification.tif'
tifs = glob.glob(os.path.join(inference_dir, "*_classification.tif"))

print(f"🔍 Намерени {len(tifs)} класификационни TIFF файла. Стартиране на миграция...")

# Обхождане на всеки намерен TIF файл
for tif in tifs:
    # Генериране на пътя за новия PNG файл
    png = tif.replace(".tif", ".png")
    # Проверка дали PNG файлът вече съществува
    if not os.path.exists(png):
        try:
            # Извикване на функцията bake_png от image_processor.py
            bake_png(tif, png)
        except Exception as e:
            # Прихващане и отпечатване на евентуална грешка
            print(f"❌ Неуспешно създаване на PNG за {tif}: {e}")
    else:
        # Ако файлът съществува, го пропускаме
        print(f"⏭️ Файлът {png} вече съществува. Пропускане.")

print("🏁 Миграцията завърши.")
